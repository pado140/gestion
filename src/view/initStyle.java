    private Object[] exist(String style){
        Object[] data=null;
        String requete="select * from proto_style where style=?";
        ResultSet rs=conn.select(requete, style);
        try {
            
            while(rs.next()){
                data=new Object[7];
                data[0]=rs.getInt("proto_id");
                data[1]=rs.getString("style");
                data[2]=rs.getString("proto");
                data[3]=rs.getString("styfam");
                data[4]=rs.getString("profam");
                data[5]=rs.getString("description");
                data[6]=rs.getString("brand");
                proto_id=rs.getInt("proto_id");
            }
        } catch (SQLException ex) {
            Logger.getLogger(initStyle.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        return data;
    }

