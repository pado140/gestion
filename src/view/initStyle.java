    public void alerter(Object... ob) {
        obs.stream().forEach((o) -> {
            o.executer(ob);
        });
    }

    @Override
    public void executer(Object... obs) {
        if(obs[0].equals("fill_tab")){
            tbm.addRow(new Object[]{obs[2],obs[1],type.get(type_list.getSelectedItem().toString()),type_list.getSelectedItem().toString(),"new"});
        }
    }
    
    private void init(){
        String requete="select * from type";
        ResultSet rs=conn.select(requete);
        Object[] data=null;
        txt_proto.setText(null);
        txt_pattern.setText(null);
        txt_profam.setText(null);
        tbm.setNumRows(0);
        type=new HashMap<>();
        type_list.addItem("-- Select Fabric Type --");
        type_list.setSelectedIndex(0);
        try {
            while(rs.next()){
                type.put(rs.getString("TYPE"),rs.getInt("TYPE_ID"));
                type_list.addItem(rs.getString("TYPE"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(NEW_Style.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
