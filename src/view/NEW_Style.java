    private void load(int proto){
        String requete="select * from stylemaster where proto_id=?";
        ResultSet rs=conn.select(requete, proto);
        Object[] data=null;
        try {
            while(rs.next()){
                data=new Object[4];
                data[0]="fill";
                data[2]=rs.getInt("fab_id");
                data[1]=rs.getString("fabnm");
                String label=rs.getString("type").toLowerCase().trim();
                if(rs.getString("type").toLowerCase().startsWith("outter")){
                    label="o";
                    label+=rs.getString("type").toLowerCase().trim().split(" ")[1];
                }
                if(rs.getString("type").toLowerCase().startsWith("inner")){
                    label="i";
                    label+=rs.getString("type").toLowerCase().trim().split(" ")[1];
                }
                label+="_fab";
                data[3]=label;
                alerter(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(NEW_Style.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
