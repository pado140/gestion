    private Object[] exist(String style){
        Object[] data=null;
        String requete="select * from proto_style where style=?";
        ResultSet rs=conn.select(requete, style);
        try {
            
            while(rs.next()){
                data=new Object[7];
                data[0]=rs.getInt("proto_id");
                data[1]=rs.getString("style");
                data[2]=rs.getString("proto");
                data[3]=rs.getString("styfam");
                data[4]=rs.getString("profam");
                data[5]=rs.getString("description");
            }
        } catch (SQLException ex) {
            Logger.getLogger(NEW_Style.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        return data;
    }
    private boolean cansave(){
        if(!(txt_style.getText().trim().isEmpty()||txt_pattern.getText().trim().isEmpty()||
                txt_proto.getText().trim().isEmpty()||txt_profam.getText().trim().isEmpty()||DESC.getText().trim().isEmpty()
                ||CUST.getSelectedIndex()<1)){
            if(!(main.isSelected()||contrast.isSelected()||insert.isSelected()||fusing.isSelected()||
                    bop.isSelected()||pocketing.isSelected()))
                return false;
            if(revers.isSelected()){
                if(main.isSelected()){
               
                    if(txt_imain.getText().trim().isEmpty())
                        return false;
                
                    if(txt_omain.getText().trim().isEmpty())
                        return false;
                }
                if(contrast.isSelected()){
                
                    if(txt_icont.getText().trim().isEmpty())
                        return false;
                
                    if(txt_ocont.getText().trim().isEmpty())
                        return false;
                }
                if(insert.isSelected()){
                    if(txt_iinsert.getText().trim().isEmpty())
                        return false;
                    if(txt_oinsert.getText().trim().isEmpty())
                        return false;
                }
            }else{
                if(main.isSelected()){
                    if(txt_main.getText().trim().isEmpty())
                        return false;
                }
                if(contrast.isSelected()){
                    if(txt_cont.getText().trim().isEmpty())
                        return false;
                }
                if(insert.isSelected()){
                    if(txt_insert.getText().trim().isEmpty())
                         return false;
                }
                if(fusing.isSelected()){
                    if(txt_fusing.getText().trim().isEmpty())
                        return false;
                }
                if(pocketing.isSelected()){
                    if(txt_pocket.getText().trim().isEmpty())
                        return false;
                }
                if(bop.isSelected()){
                    if(txt_bop.getText().trim().isEmpty())
                        return false;
                }
            }
           return true; 
        }
        return false;
    }
