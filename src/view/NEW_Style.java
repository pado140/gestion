    private void save(){
        String requete="insert into proto_style (style,proto,brand,styfam,profam,description) values("
                + "?,?,?,?,?,?)";
        if(conn.Update(requete, 1, txt_style.getText().trim(),txt_proto.getText().trim(),CUST.getSelectedItem().toString(),
                txt_pattern.getText().trim(),txt_profam.getText().trim(),DESC.getText().trim())){
            int style_id=conn.getLast();
            requete="insert into fab_style(proto_id,fab_id,type_id,proto_opt) values (?,?,?,1)";
            if(revers.isSelected()){
                if(main.isSelected()){
               
                    conn.Update(requete, 0 , style_id,fab.get(txt_imain.getText().trim()),8);
                        
                    conn.Update(requete, 0 , style_id,fab.get(txt_omain.getText().trim()),9);
                }
                if(contrast.isSelected()){
                
                    conn.Update(requete, 0 , style_id,fab.get(txt_icont.getText().trim()));
                        
                    conn.Update(requete, 0 , style_id,fab.get(txt_ocont.getText().trim()));
                }
                if(insert.isSelected()){
                    conn.Update(requete, 0 , style_id,fab.get(txt_iinsert.getText().trim()));
                        
                    conn.Update(requete, 0 , style_id,fab.get(txt_oinsert.getText().trim()));
                }
            }else{
                if(main.isSelected()){
                    conn.Update(requete, 0 , style_id,fab.get(txt_main.getText().trim()),2);
                }
                if(contrast.isSelected()){
                    conn.Update(requete, 0 , style_id,fab.get(txt_cont.getText().trim()),3);
                }
                if(insert.isSelected()){
                    conn.Update(requete, 0 , style_id,fab.get(txt_insert.getText().trim()),5);
                }
                if(fusing.isSelected()){
                    conn.Update(requete, 0 , style_id,fab.get(txt_fusing.getText().trim()),4);
                }
                if(pocketing.isSelected()){
                    conn.Update(requete, 0 , style_id,fab.get(txt_pocket.getText().trim()),6);
                }
                if(bop.isSelected()){
                    conn.Update(requete, 0 , style_id,fab.get(txt_bop.getText().trim()),7);
                }
            }
        }
    }

