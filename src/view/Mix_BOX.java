    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(workorder.containsKey(txt_po.getText().trim().concat(".").concat(txt_sku.getText().trim()))){
            if((Integer)qty.getValue()>0){
                String order=workorder.get(txt_po.getText().trim().concat(".").concat(txt_sku.getText().trim()));
                int nb=load_lpn(order);
                String stickers=order+qty.getValue();
                stickers+=nb;
                tbm.addRow(new Object[]{txt_sku.getText().trim(),qty.getValue(),stickers,order});
            }else{
                JOptionPane.showMessageDialog(this, "please insert a qty");
                return;
            }
            
        }else{
            JOptionPane.showMessageDialog(this, "This sku is not in this po");
                return;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

