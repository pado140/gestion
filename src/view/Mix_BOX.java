    private void insert(String lpn_mix){
        String requete="insert into mix_box (lpn_mix) values(?)";
        String req1="insert into box_contain(ordnum,LPN,box_stickers,qty,boxmix_id) values (?,?,?,?,?)";
        if(conn.Update(requete, 1, lpn_mix)){
            int mix_id=conn.getLast();
            for(int i=0;i<grid.getRowCount();i++){
                conn.Update(req1, 0, grid.getValueAt(i, 3),grid.getValueAt(i, 2),grid.getValueAt(i, 2),grid.getValueAt(i, 1),mix_id);
            }
        }
    }
    
